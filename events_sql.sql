-- 创建 'events' 表
CREATE TABLE "events" (
    -- 事件ID，作为主键自动递增
    "event_id" INTEGER PRIMARY KEY AUTOINCREMENT,
    -- 事件的描述或信息
    "event_info" TEXT NULL,
    -- 事件所属的地图ID
    "map_id" INTEGER NULL,
